package day02_escapeSequences;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello Students \n Today we have a Java Class");

        System.out.println("--------------------------------------");

        System.out.println("\tJava Programming Language");
        System.out.println("\t\t Java Developer Course Batch 1");

        System.out.println("--------------------------------------");

        System.out.println("\\");

        System.out.println("My favorite show is \"The Walking Dead\"");

        // \n = New Line
        // \t = Paragraph space
        // \\ = back slash
        // \"= double quote

    }


}
